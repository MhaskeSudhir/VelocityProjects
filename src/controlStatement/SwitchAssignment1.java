package controlStatement;

public class SwitchAssignment1 {

	public static void main(String[] args) {
		
		int month=2;
		
		switch (month) {
		case 1:  System.out.println("JANUARY MONTH OF THE YEAR");
	    break;
	    
		case 2: System.out.println("FEBRUARY MONTH OF THE YEAR");
		break;
		
		case 3: System.out.println("MARCH MONTH OF THE YEAR");
		break;
		
		case 4: System.out.println("APRIL MONTH OF THE YEAR");
		break;
		
		case 5: System.out.println("MAY MONTH OF THE YEAR");
		break;
		
		case 6: System.out.println("JUNE MONTH OF THE YEAR");
		break;
		
		case 7: System.out.println("JULY MONTH OF THE YEAR");
		break;
		
		case 8: System.out.println("AUGUST MONTH OF THE YEAR");
		break;
		
		case 9: System.out.println("SEPTEMBER MONTH OF THE YEAR");
		break;
		
		case 10: System.out.println("OCTOBER MONTH OF THE YEAR");
		break;
		
		case 11: System.out.println("NOVEMBER MONTH OF THE YEAR");
		break;
		
		case 12: System.out.println("DECEMBER MONTH OF THE YEAR");
		break;

		default: System.out.println("INVALID MONTH ENTERED");
			break;
		}

	}

}
