package castingStudy;

public class ImplicitCasting {

	public static void main(String[] args) {
		
		// CONVERSION--- LOWER DATATYPE TO HIGHER DATATYPE
		// MEMORY INCREASING--WIDENING CASTING
		// NO DATA LOSS TAKES PLACE
		
		// INT TO DOUBLE--- CONVERSION
		
		int a=15555;
		
		double b=a;
		
		// SHORT TO DOUBLE-- CONVERSION
		
		short p=125;
		
		double q=p;
		
		System.out.println("VALUE OF B IS "+b);
		System.out.println("VALUE OF Q IS "+q);
		
		// DATATYPE MEMORY SIZES
		//byte		1 byte		
		//short		2 bytes		
		//int		4 bytes	
		//long		8 bytes	
		//float     4 bytes
		//double    8 bytes
		//char		2 bytes



	}

}
