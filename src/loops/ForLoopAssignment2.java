package loops;

public class ForLoopAssignment2 {

	public static void main(String[] args) {
		System.out.println("15 TABLE IN FORWARD");
		System.out.println();
		
		for (int i=15 ; i<=150 ; i=i+15)
		{
			
			System.out.println(i);
		}
		System.out.println();
		System.out.println("========================================");
		System.out.println();
		System.out.println("15 TABLE IN BACKWARD");
		System.out.println();
		
		for (int i=150 ; i>=15 ; i=i-15)
		{
			
			System.out.println(i);
		}

	}

}
