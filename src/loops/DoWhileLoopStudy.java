package loops;

public class DoWhileLoopStudy {

	public static void main(String[] args) {
		
	// TO PRINT COUNT FROM 1 TO 10
		
		int i=1;
		
		// DO lOOP IS EXECUTED ONCE WHETHER CONDITION TRUE OR FALSE
		
		do  // do LOOP EXECUTE ONCE AT LEAST
		{
			System.out.println(i);  // 1-2-3------------10
			
			i++;   //2-3-4------10
		}
		while(i<=10);
		
		System.out.println("OUT OF DO WHILE LOOP");

	}

}
