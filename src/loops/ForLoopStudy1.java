package loops;

public class ForLoopStudy1 {

	public static void main(String[] args) {
		// I WANT TO PRINT NUMBER FROM 10 TO 1 (DESENDING0---->DECREMENT
		// ITERATES (REPEATS) PART OF PROGRAM MULTIPLE TIMES
		//IN FOR LOOP NUMBER OF ITERATION IS FIXED
		// WHEN LOOP EXECUTED COMPLETELY FIRST-----THEN VALUE OF I UPDATED AT LAST FOR NEXT EXECUTION
		
		for (int i=10; i>=1 ; i--)
		{
			System.out.println(i);
		}

	}

}
