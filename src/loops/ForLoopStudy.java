package loops;

public class ForLoopStudy {

	public static void main(String[] args) {
		// I WANT TO PRINT 1 TO 10 NUMBER---->INCR/DECR
		// CONDITION GIVES BOOLEAN OUTPUT---SO '==' OPERATOR IS NOT USED
		//I++---> INCREMENT BY 1---->i.e. I=I+1
		
		// for ( INITIALIZATION ; CONDITION ; UPDATION )-----SYNTAX
		
		for (int i=1 ; i<=10 ; i++) //int i=1-->Initialization ; i<=10-->condition ; i++ -->Updation
		{
			System.out.println(i);
		}

	}

}
